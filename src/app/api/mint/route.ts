// Contract address
const contractAddress = process.env.CONTRACT_ADDRESS || '';

export async function POST(req: Request) {

  try {
    console.log("rpc = ", rpcUrl);
    
    
    const wallet = new ethers.Wallet(privateKey, provider);

    // Create a contract instance
    const contract = new ethers.Contract(contractAddress, contractABI, wallet);

    // Extract mint parameters from the request body
    //const { to, id, amount, data } = req.body;
    const { to, id, amount, data } = (await req.json()) as {
      to: string;
      id: string;
      amount: string;
      data: string;
    };

    // Call the mint function
    const tx = await contract.mint(to, id, amount, data || '0x');
    console.log('Transaction hash:', tx.hash);

    // Wait for the transaction to be mined
    const receipt = await tx.wait();
    console.log('Transaction was mined in block:', receipt.blockNumber);

    // Send the response
    
    return NextResponse.json({ message: "Mint done", data: { transactionHash: tx.hash, blockNumber: receipt.blockNumber } })
  } catch (error) {
    console.log("rpc = ", rpcUrl);
    console.log("contractAddress  ->", contractAddress);
    console.log("private key  ->", privateKey);
    console.log("error ------->", error)
    return NextResponse.json({
      message: "Please Try Again Later",
      error
    },{
      status: 500
    })
  }
}

